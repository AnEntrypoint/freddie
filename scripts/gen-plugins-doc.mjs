#!/usr/bin/env node
// Generates PLUGINS.md and website/content/pages/plugins.yaml from the live
// plugin registry (src/host/index.js bootHost -> host.plugins() + host.pi.tools.list()).
// Source of truth for enumeration is discoverPlugins()/bootHost() itself, not a
// directory walk, so this stays correct if the loader's rules change.
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { bootHost, host } from '../src/host/index.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')

function categoryOf(name) {
    if (name.startsWith('tool-')) return 'tool'
    if (name.startsWith('platform-')) return 'platform'
    if (name.startsWith('memory-')) return 'memory'
    if (name.startsWith('gui-')) return 'gui'
    if (name.startsWith('core-')) return 'core'
    return 'other'
}

async function main() {
    await bootHost()
    const h = host()
    const plugins = h.plugins() // [{name, version, surfaces, requires}]
    const tools = h.pi.tools.list() // [{name, toolset, schema:{description,...}, requiresEnv?}]
    const toolByDir = new Map(tools.map(t => [t.name, t]))

    const rows = plugins.map(p => {
        const dirName = p.name.startsWith('tool-') ? p.name.slice('tool-'.length) : p.name
        const tool = toolByDir.get(dirName)
        return {
            name: p.name,
            dir: dirName,
            category: categoryOf(p.name),
            toolset: tool?.toolset || (p.name.startsWith('tool-') ? 'core' : ''),
            description: tool?.schema?.description || '',
            requiresEnv: tool?.requiresEnv || [],
            requires: p.requires || [],
            surfaces: p.surfaces,
        }
    }).sort((a, b) => a.category.localeCompare(b.category) || a.name.localeCompare(b.name))

    writeMarkdown(rows, plugins.length, tools.length)
    writeYaml(rows, plugins.length, tools.length)

    console.log(`plugins discovered: ${plugins.length}`)
    console.log(`tools registered:   ${tools.length}`)
    console.log('wrote PLUGINS.md')
    console.log('wrote website/content/pages/plugins.yaml')
    process.exit(0)
}

function writeMarkdown(rows, pluginCount, toolCount) {
    const byCat = groupBy(rows, r => r.category)
    const catOrder = ['tool', 'platform', 'memory', 'gui', 'core', 'other']
    const catLabel = { tool: 'Tools', platform: 'Platform adapters', memory: 'Memory providers', gui: 'GUI / dashboard', core: 'Core', other: 'Other' }

    let out = `# Plugins\n\nAuto-generated by \`scripts/gen-plugins-doc.mjs\`. Do not edit by hand — re-run the script instead.\n\n`
    out += `${pluginCount} plugins discovered by \`bootHost()\`, registering ${toolCount} tools.\n\n`

    for (const cat of catOrder) {
        const list = byCat.get(cat)
        if (!list || !list.length) continue
        out += `## ${catLabel[cat]} (${list.length})\n\n`
        out += `| Name | Toolset | Description | Requires env | Surfaces |\n`
        out += `| --- | --- | --- | --- | --- |\n`
        for (const r of list) {
            const desc = (r.description || '').replace(/\|/g, '\\|')
            const env = r.requiresEnv.length ? r.requiresEnv.join(', ') : ''
            out += `| \`${r.dir}\` | ${r.toolset || ''} | ${desc} | ${env} | ${r.surfaces} |\n`
        }
        out += `\n`
    }
    fs.writeFileSync(path.join(ROOT, 'PLUGINS.md'), out)
}

function yamlEscape(s) {
    if (s == null) return '""'
    const str = String(s)
    if (str === '') return '""'
    return `"${str.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`
}

function writeYaml(rows, pluginCount, toolCount) {
    const byCat = groupBy(rows, r => r.category)
    const catOrder = ['tool', 'platform', 'memory', 'gui', 'core', 'other']
    const catLabel = { tool: 'tools', platform: 'platform adapters', memory: 'memory providers', gui: 'gui / dashboard', core: 'core', other: 'other' }
    const catLede = {
        tool: 'agent-callable tools registered via pi.tools.register.',
        platform: 'gateway adapters exposing an EventEmitter-based platform class.',
        memory: 'memory providers exposing syncTurn + prefetch.',
        gui: 'dashboard routes and pages under plugins/gui-*.',
        core: 'core subsystem plugins (cli, cron, commands, skills, ...).',
        other: 'uncategorized plugins.',
    }

    let y = `title: Plugins\n`
    y += `slug: plugins\n`
    y += `hero:\n`
    y += `  heading: plugins\n`
    y += `  subheading: every subsystem in freddie is a plugin against one universal contract\n`
    y += `  accent: pi for headless, gui for the dashboard, both for full-stack.\n`
    y += `  body: >-\n`
    y += `    Auto-generated reference of all ${pluginCount} plugins discovered by bootHost(), registering ${toolCount} tools in total.\n`
    y += `    Regenerate with \`node scripts/gen-plugins-doc.mjs\`.\n`
    y += `  badges:\n`
    y += `    - { label: "${pluginCount}", desc: in-tree plugins }\n`
    y += `    - { label: "${toolCount}", desc: registered tools }\n`
    y += `    - { label: "1 contract", desc: pi + gui }\n`
    y += `    - { label: surface guard, desc: enforced at load }\n`
    y += `  ctas:\n`
    y += `    - { label: contract source, href: "https://github.com/AnEntrypoint/freddie/blob/master/src/host/contract.js" }\n`
    y += `    - { label: host source, href: "https://github.com/AnEntrypoint/freddie/blob/master/src/host/host.js" }\n`
    y += `sections:\n`
    for (const cat of catOrder) {
        const list = byCat.get(cat)
        if (!list || !list.length) continue
        y += `  - id: ${cat}\n`
        y += `    name: ${catLabel[cat]} (${list.length})\n`
        y += `    lede: ${yamlEscape(catLede[cat])}\n`
        y += `    features:\n`
        for (const r of list) {
            y += `      - name: ${yamlEscape(r.dir)}\n`
            y += `        desc: ${yamlEscape(r.description || r.name)}\n`
            if (r.requiresEnv.length) y += `        benefit: ${yamlEscape('requires: ' + r.requiresEnv.join(', '))}\n`
        }
    }
    fs.mkdirSync(path.join(ROOT, 'website/content/pages'), { recursive: true })
    fs.writeFileSync(path.join(ROOT, 'website/content/pages/plugins.yaml'), y)
}

function groupBy(arr, fn) {
    const m = new Map()
    for (const x of arr) {
        const k = fn(x)
        if (!m.has(k)) m.set(k, [])
        m.get(k).push(x)
    }
    return m
}

main().catch(e => { console.error(e); process.exit(1) })
